import USERLIST from '../_mock/user';

// ----------------------------------------------------------------------

const TABLE_HEAD = [
  { id: 'name', label: 'Document Name', alignRight: false },
  { id: 'instansi', label: 'Instansi', alignRight: false },
  { id: 'status', label: 'Status', alignRight: false },
  { id: '' },
];

